    private void bAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAdicionarActionPerformed
        if (verificaCampos() == true){
            objeto = new CursosMatriculados();
            objeto.setMatricula(objMatricula);
            objeto.setCurso(cursos.get(cCurso.getSelectedIndex()));
          
            prod = cursos.get(cCurso.getSelectedIndex());
           
            objMatricula.getCursosMatriculados().add(objeto);
            montaTabela();
            limpaCampos();
        }
    }//GEN-LAST:event_bAdicionarActionPerformed

