    public void montaTabela(){
        
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Curso");
        modelo.addColumn("Duração");
        

        for (Iterator<CursosMatriculados> it = objMatricula.getCursosMatriculados().iterator(); it.hasNext();){
            CursosMatriculados cursosMatriculados = it.next();
            modelo.addRow(new Object[]{cursosMatriculados.getCurso().getNome(), cursosMatriculados.getCurso().getDuracao()});
        }
        tabelaCursos.setModel(modelo);
    }


    public boolean verificaCampos(){
       if (cCurso.getSelectedItem() == null){
            JOptionPane.showMessageDialog(null,
                    "O campo Curso deve ser preenchio!",
                    "Atenção", JOptionPane.WARNING_MESSAGE);
            cCurso.requestFocus();
            return false;
        } else {
            return true;
        }
    }

    public void limpaCampos(){
        
       
        cCurso.setSelectedItem(null);
    }

