    private void montaCombo() {
        cursos = conProd.listaTodos();
        cCurso.removeAllItems();
        for (Iterator<Curso> it = cursos.iterator(); it.hasNext();) {
            Curso curso= it.next();
            cCurso.addItem(curso.getNome());
    }
    }
