    public void montaTabela() {
    if(cOpcao.getSelectedIndex() == 0){
            lista = controle.montaListaObjetoClientePlano(fFiltro.getText());
        }else if(cOpcao.getSelectedIndex() == 1){
            lista = controle.montaListaObjetoPlanoInternet(fFiltro.getText());

        } else {lista = controle.listaTodos();
        }
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Cliente");
        modelo.addColumn("Plano");
        modelo.addColumn("Valor");
        
        for (Iterator<VendaPlano> it = lista.iterator(); it.hasNext();) {
            VendaPlano vendaPlano = it.next();
            modelo.addRow(new Object[]{
                        vendaPlano.getCliente().getNome(),
                        vendaPlano.getPlanoInternet().getDescricao(),
                        vendaPlano.getPlanoInternet().getValor()});
        }
        tabela.setModel(modelo);
    }

    public void validaBotoes(String acao) {
        if (acao.equals("selecionado")) {
            cPlano.setEnabled(false);
            cCliente.setEnabled(false);
            cPlano.setEnabled(false);
       
            bNovo.setEnabled(true);
            bAlterar.setEnabled(true);
            bExcluir.setEnabled(true);
            bSalvar.setEnabled(false);
            bCancelar.setEnabled(false);
            bSair.setEnabled(true);
            jLabel5.setEnabled(false);
            jLabel6.setEnabled(false);
            jLabel8.setEnabled(false);
        } else if (acao.equals("novo") || acao.equals("alterar")) {
            cPlano.setEnabled(true);
            cPlano.setEnabled(true);
             cCliente.setEnabled(true);
            bNovo.setEnabled(false);
            bAlterar.setEnabled(false);
            bExcluir.setEnabled(false);
            bSalvar.setEnabled(true);
            jLabel5.setEnabled(true);
            jLabel6.setEnabled(true);
            jLabel8.setEnabled(true);
            bCancelar.setEnabled(true);
            bSair.setEnabled(false);
            tabela.setEnabled(false);
        } else if (acao.equals("inicio")
                || acao.equals("cancelar") || acao.equals("salvar")) {
           cPlano.setEnabled(true);
            cPlano.setEnabled(false);
            cCliente.setEnabled(false);
           
           bNovo.setEnabled(true);
            bAlterar.setEnabled(false);
            bExcluir.setEnabled(false);
            bSalvar.setEnabled(false);
            bCancelar.setEnabled(false);
            bSair.setEnabled(true);
            tabela.setEnabled(true);
            jLabel5.setEnabled(false);
            jLabel6.setEnabled(false);
            jLabel8.setEnabled(false);
        }
    }

    public boolean verificaCampos() {
        if (cCliente.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null,
                    "O campo Cliente deve ser preenchio!",
                    "Atenção", JOptionPane.WARNING_MESSAGE);
            cCliente.requestFocus();
            return false;
        } else {
            return true;
        }
    }

    public void limpaCampos() {
        cPlano.setSelectedItem(null);
        fId.setText(null);
        cPlano.setSelectedItem(null);
        cCliente.setSelectedItem(null);

    }

