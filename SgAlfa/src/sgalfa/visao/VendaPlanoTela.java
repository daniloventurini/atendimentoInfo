    private void montaComboCliente() {
        clientes = cliControle.listaTodos();
        cCliente.removeAllItems();
        for (Iterator<Cliente> it = clientes.iterator(); it.hasNext();) {
            Cliente cliente = it.next();
            cCliente.addItem(cliente.getNome());
        }
    }


    private void montaComboPlano() {
        planos = planoControle.listaTodos();
        cPlano.removeAllItems();
        for (Iterator<PlanoInternet> it = planos.iterator(); it.hasNext();) {
            PlanoInternet plano = it.next();
            cPlano.addItem(plano.getDescricao());//descrição = nome
        }
    }
