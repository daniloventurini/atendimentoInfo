    public BigDecimal getTotalGeral(){
        BigDecimal totalGeral = new  BigDecimal(0);
        for (Iterator<ItensVenda> it = obj.getItensVenda().iterator(); it.hasNext();) {
            ItensVenda iv = it.next();
            totalGeral = totalGeral.add(iv.getTotal());
        }
        return totalGeral;
    }

    

