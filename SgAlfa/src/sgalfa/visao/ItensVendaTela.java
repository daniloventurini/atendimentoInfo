    public void montaComboProduto(){
        produtos = new ArrayList<Produto>();
        produtos = conPro.listaTodos();
        cProduto.removeAllItems();
        for (Iterator<Produto> it = produtos.iterator(); it.hasNext();) {
            Produto c = it.next();
            cProduto.addItem(c.getNomeProduto());
        }
    }

    public void montaTabela(){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Produto");
        modelo.addColumn("Quat.");
        modelo.addColumn("Valor");
        modelo.addColumn("Total");

        for (Iterator<ItensVenda> it = obj.getItensVenda().iterator(); it.hasNext();) {
            ItensVenda iv = it.next();
            modelo.addRow(new Object[]{iv.getProduto().getNomeProduto(),
            iv.getQuantidade(), iv.getValor(), iv.getTotal()});
        }
            tabela.setModel(modelo);
    }

