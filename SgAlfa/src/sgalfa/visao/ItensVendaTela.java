    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        objeto = new ItensVenda();
        objeto.setVenda(obj);
        objeto.setProduto(produtos.get(cProduto.getSelectedIndex()));
        objeto.setQuantidade(Integer.parseInt(fQuantidade.getText()));
        String val = fValor.getText().replace(".", "");
        val = val.replace(",", ".");
        objeto.setValor(new BigDecimal(val));
        objeto.setTotal();
        obj.getItensVenda().add(objeto);
        montaTabela();
        cProduto.setSelectedItem(null);
        fQuantidade.setText(null);
        fValor.setText(null);
    }//GEN-LAST:event_jButton1ActionPerformed

